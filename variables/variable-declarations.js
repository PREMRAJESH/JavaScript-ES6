// Variable Declarations and types
// Learn about var, let, and const.
// Using var (function-scoped)
var name = "Prem";
console.log(name);

// Using let (block-scoped)
let age = 21;
console.log(age);
age = 22;
console.log(age); //this will reassign the value of age

// Using const (block-scoped, constant value) it cannot be changed
const gender = "male";
console.log(gender);
